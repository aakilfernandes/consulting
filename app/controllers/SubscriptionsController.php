	{
		$subscription = Subscription::find($id);
		if(!$subscription)
			App::abort(404,'Subscription not found');
		
		if($subscription->user_id != Auth::user()->id)
			App::abort(404,'Subscription not found');

		$subscription->fill(Input::all());
		$subscription->save();
	}


